      Yoon Se-ri (Son Ye-jin), a South Korean chaebol heiress to Queen's Group, 
      and Ri Jeong-hyeok (Hyun Bin), a member of the North Korean elite and a captain in Korean People's Army.
      <br>
      <br>
      One day, while Yoon Se-ri is paragliding in Seoul, South Korea, a sudden tornado knocks her out and blows her off course.
      She awakens to find that her paraglider has crashed into a tree in a forest in the North Korean portion of the DMZ, an area forbidden for South Koreans. 
      She then meets Ri Jeong-hyeok, who eventually gives her shelter and develops plans to secretly help her return to South Korea.
      Over time, they fall in love, despite the divide and dispute between their respective countries.
      <br>
      <br>
      Back in South Korea, Yoon Se-ri's family suppresses the news of her disappearance out of fear that it will depress the stock price of the chaebol.
      Se-ri went missing, her retiring father informed his family that he intended to announce her as his successor based on her abilities as a businesswoman,
      as shown by her successfully building her own company, 
      instead of her half-brothers, Se-jun and Se-hyung, who struggled managing the chaebol's subsidiaries.
      The brothers are supported by their equally-ambitious wives, Do Hye-ji (Hwang Woo-seul-hye) and Go Sang-ah (Yoon Ji-min), respectively. 
      In Se-ri's absence, Se-hyung outmaneuvers his siblings through unscrupulous means to win the succession battle to lead the family firm,
      while Sang-ah attempts to take over Se-ri's company.
      <br>
      <br>
      Se-ri and Jeong-hyeok's story is intertwined with that of Seo Dan (Seo Ji-hye) and Gu Seung-jun (Kim Jung-hyun).
      Dan is the sophisticated daughter of a wealthy North Korean department store owner. 
      he has been studying the cello in Russia for several years but returns to marry Jeong-hyeok, 
      to whom she is engaged through an arranged marriage even though they have only met a few times.
      As she returns to Pyongyang, she crosses paths more than once with Gu Seung-jun, 
      who had fled to North Korea (under the protection of corrupt North Korean officers) in order to escape from the pursuit of Se-hyung,
      under whose incompetent watch he had embezzled large amounts of money.
      <br>
      <br>
      The first half of the story follows Jeong-hyeok's attempts to hide Se-ri and help her get back to South Korea. 
      They are impeded by Cho Cheol-gang (Oh Man-seok), a corrupt and ruthless officer from the State Security Department,
      who had previously arranged for the murder of Jeong-hyeok's older brother, 
      an officer who tried to expose him for his traitorous deeds against the North Korean regime.
      Jeong-hyeok's attachment to Se-ri distresses not only Dan, his fiancée, but also his father, 
      who is a high-ranking political figure, 
      as the discovery of Jeong-hyeok harboring a South Korean citizen could be used by rival officials to ruin their family's reputation.
      <br>
      <br>
      In the second half of the story, Se-ri is able to return to South Korea and resume leadership of her company, 
      surprising her family and others who had thought she was dead.
      While Cheol-gang is initially convicted and sentenced to life imprisonment for his crimes,
      he escapes custody through aide from his loyal soldiers and criminal network whereby he infiltrates South Korea to go after Se-ri.
      Despite opposition from his father who nevertheless reluctantly gives his support, 
      Jeong-hyeok first arrives in South Korea discreetly before his soldiers are clandestinely sent to Seoul by Jeong-hyeok’s father
      to assist Jeong-hyeok in his aim to protect Se-ri from Cheol-gang's vengeance. 
      Meanwhile, in North Korea, despite Seo Dan and Seung-jun's initial encounters being random and unpleasant,
      she shelters him when the corrupt officers betray him to Se-hyung's gangsters, and they eventually fall in love.
