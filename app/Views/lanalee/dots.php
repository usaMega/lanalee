      while also dealing with a South Korean warlord who once fought alongside Si-jin but now takes advantage of the corrupt government of Uruk to make money off human and arms trafficking. 
      While in Uruk, Mo-yeon accidentally confesses her feelings (much to the amusement of their respective teams) and the two officially start dating. She gradually comes to terms with the possibility of him being killed in action and the fact that she may never have full disclosure of his whereabouts. 
